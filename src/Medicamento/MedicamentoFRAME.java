    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        Medicamento objMedicamento = new Medicamento();
        int id = Integer.parseInt(txtid.getText());
        String nombre = txtNombres.getText();
        String laboratorio = txtLaboratorio.getText();
        String descripcion = txtDescripcion.getText();
        String composicion = txtComposicion.getText();
        String medicamento = txtMedicamento.getText();

        boolean resultado = objMedicamento.insertarMedicamento(id, nombre, laboratorio, descripcion, composicion, medicamento);
        if(resultado){
            JOptionPane.showMessageDialog(null, "Se inserto correctamente.");
            cargarTabla();
        }else{
            JOptionPane.showMessageDialog(null, "Ocurrio un error al insertar.");
        }
         txtid.setText("");
        txtNombres.setText("");
        txtLaboratorio.setText("");
        txtDescripcion.setText("");
        txtComposicion.setText("");
        txtMedicamento.setText("");
        txtid.requestFocus();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void jtMedicamentoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtMedicamentoMouseClicked
        int aux = jtMedicamento.getSelectedRow();
        txtid.setText(modeloTabla.getValueAt(aux, 0).toString());
        txtNombres.setText(modeloTabla.getValueAt(aux, 1).toString());
        txtLaboratorio.setText(modeloTabla.getValueAt(aux, 2).toString());
        txtDescripcion.setText(modeloTabla.getValueAt(aux, 3).toString());
        txtComposicion.setText(modeloTabla.getValueAt(aux, 4).toString());
        txtMedicamento.setText(modeloTabla.getValueAt(aux, 5).toString());

    }//GEN-LAST:event_jtMedicamentoMouseClicked

